            <span class="history-card__label">Histórico</span>
            <h2>Histórico de Curuçá</h2>
            <p>Curuçá, teve sua fundação em decorrência da provisão régia de 23 de setembro de 1652, sancionada pelo então rei de Portugal Dom João IV, autorizando a Companhia de Jesus a fundar na capitania do Grão-Pará, uma missão religiosa, esta missão deram o nome de Curuçá, lançando assim os alicerces do atual município, isto em pleno meados do Século XVII, marcando a presença na região e catequizando os índios Tupinambás, primeiros habitantes do lugar.</p>
            <p>Vale ressaltar que a princípio a povoação localizou-se as margens do rio Maripanema hoje Muriá, no lugar atualmente conhecido como Abade, entretanto como forma de proteger a povoação contra as investidas dos colonos de outras partes que pretendiam sequestrar os índios, os Jesuítas resolveram trasladar a povoação para o local onde hoje se encontra instalada a cidade de Curuçá, às margens do rio Curuçá-Mirim como consta nos documentos manuscritos da época depositados no Arquivo Público do Estado. Mas, há também a versão popular de que a mudança teria ocorrido devido a água potável no Abade na época.</p>
            <p>Somente cem anos depois é que Curuçá, pelo advento da política do Marquês de Pombal na Amazônia, vive um lamentável momento em sua história por conta da expulsão dos padres jesuítas de Curuçá, e com isso sua elevação à condição de Vila com o nome de Vila Nova de El Rey, sendo ereto seu pelourinho a 03 de julho de 1757.</p>
            <p>Perdeu a categoria de vila em 1833, e somente em 1850 pela resolução nº 167 de 21 de novembro o governo imperial lhe restitui a categoria, agora com o nome de Vila de Curuçá. Sua emancipação política deu-se por força da Lei estadual nº 236 de 14 de maio de 1895. Ato do então governador Lauro Sodré.</p>
         </article>

         <article class="history-card">
            <span class="history-card__label">Primeira povoação</span>
            <h2>Do rio Maripanema ao Curuçá-Mirim</h2>
            <p>A princípio, a povoação localizou-se às margens do rio Maripanema, hoje Muriá, no lugar atualmente conhecido como Abade. Depois, os jesuítas trasladaram a povoação para a área onde hoje está instalada a cidade de Curuçá, às margens do rio Curuçá-Mirim.</p>
            <p>Segundo os registros históricos, a mudança teria buscado proteger a população das investidas de colonos que pretendiam sequestrar indígenas. Há também a versão popular de que a transferência ocorreu pela necessidade de água potável.</p>
         </article>

         <article class="history-card">
            <span class="history-card__label">Vila</span>
            <h2>Vila Nova de El Rey</h2>
            <p>Cerca de cem anos depois, com a política do Marquês de Pombal na Amazônia, Curuçá viveu um momento marcante: a expulsão dos padres jesuítas e sua elevação à condição de vila, com o nome de Vila Nova de El Rey.</p>
            <p>O pelourinho da vila foi erguido em 03 de julho de 1757, data que aparece com frequência nos registros históricos do município.</p>
         </article>

         <article class="history-card">
            <span class="history-card__label">Emancipação</span>
            <h2>De volta à categoria de vila</h2>
            <p>Curuçá perdeu a categoria de vila em 1833. Em 21 de novembro de 1850, pela Resolução nº 167, o governo imperial restituiu essa condição, agora com o nome de Vila de Curuçá.</p>
            <p>A emancipação política ocorreu por força da Lei Estadual nº 236, de 14 de maio de 1895, em ato do então governador Lauro Sodré.</p>
         </article>
